    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        /**
         * Al pulsar guardar se guarda el parte en la base de datos
         */
        Boolean cerrado = true;
        java.sql.Date fecha = new java.sql.Date(Calendar.getInstance().getTime().getTime());
        Parte p = new Parte(jtKmI.getText(),jtKmF.getText(),cerrado,fecha,jtGasoil.getText(),jtAutopista.getText(),jtDietas.getText(),jtOtros.getText(),jtaIncidencias.getText());
        p.setAlbaranesArray(albaranes);
        try {
            ProyectoFinal.guardarParte(p);
        } catch (SQLException ex) {
            Logger.getLogger(Partes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

