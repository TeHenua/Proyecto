    private void jbMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbMasActionPerformed
        /**
         * Al pulsar Guardar o añadir más se guarda el albaran en un arraylist para luego asociarlo al parte
         */
        String albaran = jtAlbaran.getText();
        String horaSalida = jtSalida.getText();
        String horaLlegada = jtLlegada.getText();
        String vehiculo = jtVehiculo.getText();
        
        Albaran a = new Albaran(albaran,horaSalida,horaLlegada,vehiculo);
        albaranes.add(a);
        jtAlbaran.setText("");
        jtSalida.setText("00:00");
        jtLlegada.setText("00:00");
        jtVehiculo.setText("");
        jpCerrado.setVisible(true);
    }//GEN-LAST:event_jbMasActionPerformed

